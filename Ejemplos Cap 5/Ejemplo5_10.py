""" Concatenación o suma y multiplicación de listas."""

# INICIO

# Inicialización de variables.
animales = ["perros", "gatos", "ratones"]
flores = ["nardos", 3.57, "rosas"]

# Cocatenación de las listas:
suma = animales + flores
print("La concatenacion de las listas es: \n", suma)

# Multiplicación por un entero:
resultado = 2*flores
print("\nEl resultado es: ", resultado)

