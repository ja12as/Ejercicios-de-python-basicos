""" Ejemplos usando diccionarios."""

# INICIO

# Inicialización de variables.
a = [3, 5, 6, -3, -9.45]
b = (-35, 4.67, 27.41, 98.65)
print("\nlista a = ", a)
print("\ntupla b = ", b)

# Convertir tupla a lista.
c = list(b)
print("\nLa lista c es: ", c)


# Longitud de una tupla.
lb = len(b)
print("\nLa longitud de b es: ", lb)

# Máximo de una tupla.
Mb = max(b)
print("\nEl máximo de la tupla b es: ", Mb)

# Míimo de una tupla.
mb = min(b)
print("\nEl máximo de la tupla b es: ", mb)


