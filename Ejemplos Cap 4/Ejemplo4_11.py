'''Algoritmo para seleccionar números pares.
   Se usa la instrucción continue'''

# INICIO

# Principio del ciclo for.
for x in range(1, 10):
    # Inicializa condición.
    if(x%2): # Checa si hay residuo.
        continue # Se salta la siguiente instrucción si x no es par.
    print( x, '\n') # Sólo imprime los pares < 10.

# Termina ciclo for.

