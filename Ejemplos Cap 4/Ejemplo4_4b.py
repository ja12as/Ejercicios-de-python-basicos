'''Programa: Suma de enteros.
   Usa un ciclo Para.
   Este algoritmo suma los enteros del 1 al 10.'''

# INICIO
# Inicializa la suma:

suma = 0

# Inicia el ciclo Para

for contador in range(1, 11):
    suma = suma + contador
# Termina el ciclo Para

# Despliega el resultado:
print("La suma es: ", suma)

