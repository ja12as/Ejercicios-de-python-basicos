'''Este algoritmo intercambia los valores
   de a y b'''

#INICIO
A = 3.56
B = -7.15

# A se almacena en C:
C = A # A = 3.56, B = -7.15, C = 3.56.

# B se almacena en A:
A = B # A = -7.15, B = -7.15, C = 3.56.

# C se almacena en B:
B = C # A = -7.15, B = 3.56, C = 3.56.

# Se muestra el resultado:
print("Los valores de A y B son: ")
print("El valor de A es %5.3f" %A)
print("El valor de B es %5.3f" %B)
